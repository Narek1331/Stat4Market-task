<?php

function Sum($matrix, $row, $col) {
    $sum = 0;
    $n = count($matrix);
    $m = count($matrix[0]);

    // Проверка существования соседей и их добавление к сумме
    if ($row > 0) {
        $sum += $matrix[$row - 1][$col];
    }
    if ($row < $n - 1) {
        $sum += $matrix[$row + 1][$col];
    }
    if ($col > 0) {
        $sum += $matrix[$row][$col - 1];
    }
    if ($col < $m - 1) {
        $sum += $matrix[$row][$col + 1];
    }

    return $sum;
}

// Пример использования функции
$matrix = [
    [51, 71, 1, 50],
    [13, 5, 19, 11],
    [60, 4, 11, 20],
    [13, 34, 17, 0],
    [16, 53, 1, 32]
];

// Пример вызова функции для элемента (0, 0)
echo "Сумма соседей элемента (0, 0): " . Sum($matrix, 0, 0) . "\n";

// Пример вызова функции для элемента (3, 2)
echo "Сумма соседей элемента (3, 2): " . Sum($matrix, 3, 2) . "\n";
